# Because it specifies the of operations to be done
# Categorised into 2:
# 1. Primary Data Types (Strings(text), Integers(whole numbers), Floating Points(fractions), Boolean(True/False))
# 2. Secondary: Collections -> Lists[], Tuple(), Dictionary{}, Sets{}

# Strings: Textual Data
# Enclosed inside double/single
# Sequence of Characters

string1 = "I love" + " "
